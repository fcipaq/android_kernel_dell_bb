/*
 * ov5670 device structure.
 */
struct ov5670_device {
	struct v4l2_subdev sd;
	struct media_pad pad;
	struct v4l2_mbus_framefmt format;
	struct mutex input_lock;

	struct camera_sensor_platform_data *platform_data;
	int vt_pix_clk_freq_mhz;
	int fmt_idx;
	int run_mode;
	u8 res;
	u8 type;
	struct ov5670_vcm *vcm_driver;
	struct ov5670_otp_struct current_otp;
};


enum ov5670_tok_type {
	OV5670_8BIT  = 0x0001,
	OV5670_16BIT = 0x0002,
	OV5670_32BIT = 0x0004,
	OV5670_TOK_TERM   = 0xf000,	/* terminating token for reg list */
	OV5670_TOK_DELAY  = 0xfe00,	/* delay token for reg list */
	OV5670_TOK_MASK = 0xfff0
};

/**
 * struct ov5670_reg - MI sensor  register format
 * @type: type of the register
 * @reg: 16-bit offset to register
 * @val: 8/16/32-bit register value
 *
 * Define a structure for sensor register initialization values
 */
struct ov5670_reg {
	enum ov5670_tok_type type;
	u16 reg;
	u32 val;	/* @set value for read/mod/write, @mask */
};

#define to_ov5670_sensor(x) container_of(x, struct ov5670_device, sd)

